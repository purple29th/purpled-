package com.example.purpled.album;

public class AlbumListClass {

    private String trackUrl;
    private String trackImage;
    private String trackArtist;
    private String tractAmount;
    private String trackTitle;
    private String trackAlbumName;

    public String getTrackUrl() {
        return trackUrl;
    }

    public void setTrackUrl(String trackUrl) {
        this.trackUrl = trackUrl;
    }

    public String getTrackImage() {
        return trackImage;
    }

    public void setTrackImage(String trackImage) {
        this.trackImage = trackImage;
    }

    public String getTrackArtist() {
        return trackArtist;
    }

    public void setTrackArtist(String trackArtist) {
        this.trackArtist = trackArtist;
    }

    public void setTractAmount(String tractAmount) {
        this.tractAmount = tractAmount;
    }

    public String getTractAmount() {
        return tractAmount;
    }


    public String getTrackAlbumName() {
        return trackAlbumName;
    }

    public void setTrackAlbumName(String trackAlbumName) {
        this.trackAlbumName = trackAlbumName;
    }

}
